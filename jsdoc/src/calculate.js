/**
 * Thrice module - See {@tutorial calculate-tutorial}
 * @module calculate
 */

/**
 *
 * @param {number} n1 - Take a number
 * @returns {number} - Return thrice the number
 */
exports.thrice = (n1) => n1 * 3;

/**
 *
 * @param {number} n1 - First number
 * @param {number} n2 - Second number
 * @returns {number} - Addition of n1 and n2
 */
exports.add = (n1, n2) => n1 + n2;

/**
 *
 * @param {number} n1 - First number
 * @param {number} n2 - Second number
 * @returns {number} - Subtraction of n1 and n2
 */
exports.subtract = (n1, n2) => n1 - n2;

/**
 *
 * @param {number} n1 - First number
 * @param {number} n2 - Second number
 * @returns {number} - Multiplication of n1 and n2
 */
exports.multiply = (n1, n2) => n1 * n2;

/**
 *
 * @param {number} n1 - First number
 * @param {number} n2 - Second number
 * @returns {number} - Division of n1 and n2
 */
exports.divide = (n1, n2) => n1 / n2;
